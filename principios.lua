--[[
        CONVEÇÕES 
    Para declarar uma varíavel, podemos utilizar qualquer conjunto de letras. Contudo, esse 
conjunto não pode ser iniciado por um numeral. 

        TABLES E ESTRUTURAS DE DADOS 
    Para que uma table seja criada, utilizamos o sinal de chaves ({}). De modo geral, as tabelas
em Lua são objetos, ou seja, são estruturas que têm uma propriedade como nome, sendo a chave e 
um valor associado a ela. 

        OPERADORES LÓGICOS 
and: Operador lógico "E". 
or: Operador lógico "OU". 
not: Operador lógico de negação. 

        OPERAÇÕES ARITMÉTICAS
+ , - , * , / , // , % e ^.

        OPERAÇÕES RELACIONAIS
== , ~= , < , > , <= e >=.

        TIPOS DE VALORES 
nil: Valor nulo. Esse valor é assumido nas variáveis por padrão, quando não são declarados. 
false: Valor booleano falso. 
true: Valor booleano verdadeiro. 
number: Representa os números reais e números que possuam duas casas decimais, como 3, 4.52 e 8.20. 
string: Representa conjuntos de caracteres. 

        ESTRUTURAS DE LOOP 
while: Inicia um loop que executa enquanto uma condição for verdadeira.
break: Interrompe a execução de um loop.
repeat: Inicia um loop que executa repetidamente até que uma condição seja verdadeira.
until: Parte de um loop repeat que indica a condição para encerrar o loop.
for: Inicia um loop.
in: Utilizado em loops para iterar sobre valores em uma tables.
ipairs: Iterar sobre elementos de uma tabela com índices consecutivos começando de 1.


        BLOCO DE CÓDIGO 
do: Inicia um bloco de código.
end: Encerra um bloco de código.
then: Parte de uma estrutura condicional que indica o código a ser executado se a condição for verdadeira.
if: Inicia uma estrutura condicional.
elseif: Parte de uma estrutura condicional que é uma alternativa a um "else if".
else: Parte de uma estrutura condicional que é executada se a condição for falsa.

        FUNÇÃO 
function: Define uma função.
return: Retorna valores de uma função.

